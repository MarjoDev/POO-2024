package br.edu.fatecfranca.exe3o1;

public class Message{
    public int id;
    public String text;

    public Message(){
        this.id = 0;
        this.text = "";
    }

    public Message(int id, String text){
        this.id = id;
        this.text = text;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    @Override
    public String toString() {
        return "Message{" +
                "id=" + id +
                ", text='" + text + '\'' +
                '}';
    }
}
